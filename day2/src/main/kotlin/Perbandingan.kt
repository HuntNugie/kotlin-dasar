fun main() {
//    operator perbandingan merupakan operator yang berfungsi untuk membandingkan antar 2 nilai dan akan mengembalikan true and false
    val a = 5
    val b = 6

    println("a = $a")
    println("b = $b")
    println("a > b ${a>b}")
    println("a < b ${a<b}")
    println("a >= b ${a>=b}")
    println("a <= b ${a<=b}")
    println("a == b ${a==b}")
    println("a === b ${a===b}")
    println("a != b ${a!=b}")

}